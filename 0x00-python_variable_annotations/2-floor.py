#!/usr/bin/env python3
"""Module that provides a function to calculate the floor of a float."""

import math


def floor(n: float) -> int:
    """
    Calculate and return the floor of a given float number.

    Args:
        n (float): The float number to calculate the floor of.

    Returns:
        int: The floor of the input float.
    """
    return math.floor(n)
